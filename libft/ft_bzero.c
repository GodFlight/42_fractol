#include "libft.h"

void	*ft_bzero(void *s, size_t n)
{
	int		i;
	char	*s1;

	i = 0;
	s1 = (char*)s;
	if (n == 0)
		return (0);
	while (n-- > 0)
	{
		s1[i] = '\0';
		i++;
	}
	return ((void*)s);
}
